package com.attendance.bean;

public class VoterBean {
	
	private int voterid; 
	private String voterno; 
	private int userid; 
	private String voterparty;
	
	
	public int getVoterid() {
		return voterid;
	}
	public void setVoterid(int voterid) {
		this.voterid = voterid;
	}
	public String getVoterno() {
		return voterno;
	}
	public void setVoterno(String voterno) {
		this.voterno = voterno;
	}
	public int getUserid() {
		return userid;
	}
	public void setUserid(int userid) {
		this.userid = userid;
	}
	public String getVoterparty() {
		return voterparty;
	}
	public void setVoterparty(String voterparty) {
		this.voterparty = voterparty;
	} 
	
	

}
